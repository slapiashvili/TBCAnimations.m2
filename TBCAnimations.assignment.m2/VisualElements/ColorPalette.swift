import UIKit

extension UIColor {
    static var tbcBlue: UIColor {
        return UIColor(red: 0x00 / 255.0, green: 0xAE / 255.0, blue: 0xEF / 255.0, alpha: 1.0)
    }
    
    static var tbcYellow: UIColor {
        return UIColor(red: 0xEC / 255.0, green: 0xEE / 255.0, blue: 0x0F / 255.0, alpha: 1.0)
    }
    
    static var tbcGreen: UIColor {
        return UIColor(red: 0x21 / 255.0, green: 0xF0 / 255.0, blue: 0x02 / 255.0, alpha: 1.0)
    }
    
    static var tbcPurple: UIColor {
        return UIColor(red: 0x26 / 255.0, green: 0x1A / 255.0, blue: 0xD6 / 255.0, alpha: 1.0)
    }
    
    static var tbcRed: UIColor {
        return UIColor(red: 0xB5 / 255.0, green: 0x15 / 255.0, blue: 0x09 / 255.0, alpha: 1.0)
    }
}
